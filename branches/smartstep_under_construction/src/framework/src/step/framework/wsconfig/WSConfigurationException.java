package step.framework.wsconfig;

public class WSConfigurationException extends Exception {

	private static final long serialVersionUID = 1L;

	public WSConfigurationException() {
		super();
	}

	public WSConfigurationException(String message, Throwable cause) {
		super(message, cause);
	}

	public WSConfigurationException(String message) {
		super(message);
	}

	public WSConfigurationException(Throwable cause) {
		super(cause);
	}

}
